        private readonly IAuthorizationManager authorizationManager;

        public EventsController(IAuthorizationManager authorizationManager)
        {
            this.authorizationManager = authorizationManager;
        }

        protected IQueryable<Event> GetConstraintedQuery()
        {

            UserInfo user = authorizationManager.GetCurrentUser();
            string role = authorizationManager.GetCurrentRoleName();
            IList<Constraint> constraints = user.Id.HasValue ? Context.Constraints
                .Where(x => x.UserRole.UserId.Equals(user.Id.Value) && x.UserRole.Role.SystemName.Equals(role))
                .ToList() : new List<Constraint>();
            IQueryable<Event> query = Context.Set<Event>().Where(e => !e.Disabled);

            return query;
        }

        [ClaimsAuthorize]
        [EnableQuery(MaxNodeCount = int.MaxValue)]
        public SingleResult<Event> GetEvent([FromODataUri] System.Guid key)
        {
            return SingleResult.Create(GetConstraintedQuery());
        }

        [ClaimsAuthorize]
        [EnableQuery(MaxNodeCount = int.MaxValue)]
        public IQueryable<Event> GetEvents([FromODataUri] int? clientTreeKeyId)
        {
            // выборка всех Events
            var result = GetConstraintedQuery();

            // если есть фильтр по клиенту - сделать выборку связанных с ним Events
            // иначе вернутся все Events
            if (clientTreeKeyId != null)
            {
                // ID с привязкой к клиенту
                List<Guid> eventIds = Context.Set<EventClientTree>().Where(x => x.ClientTreeId == clientTreeKeyId).Select(e => e.EventId).ToList();

                // Events без привязок
                IQueryable<Event> freeEvents = Context.Set<Event>().Where(x => !x.Disabled && !Context.Set<EventClientTree>().Any(y => y.EventId == x.Id));

                // фильтр по Event-ам с приявязкой к текущему Client и добавление Event-ов без привязок
                result = result.Where(x => eventIds.Any(e => e == x.Id)).Concat(freeEvents);
            }

            return result;
        }

        [ClaimsAuthorize]
        [HttpPost]
        public IQueryable<Event> GetFilteredData(ODataQueryOptions<Event> options)
        {
            string bodyText = Helper.GetRequestBody(HttpContext.Current.Request);
            var clientTreeKeyId = Helper.GetValueIfExists<int?>(bodyText, "clientTreeKeyId");
            var query = GetConstraintedQuery();

            var querySettings = new ODataQuerySettings
            {
                EnsureStableOrdering = false,
                HandleNullPropagation = HandleNullPropagationOption.False
            };
            var optionsPost = new ODataQueryOptionsPost<Event>(options.Context, Request, HttpContext.Current.Request);

            if (clientTreeKeyId != null)
            {
                List<Guid> eventIds = Context.Set<EventClientTree>().Where(x => x.ClientTreeId == clientTreeKeyId).Select(e => e.EventId).ToList();
                IQueryable<Event> freeEvents = Context.Set<Event>().Where(x => !x.Disabled && !Context.Set<EventClientTree>().Any(y => y.EventId == x.Id));
                query = query.Where(x => eventIds.Any(e => e == x.Id)).Concat(freeEvents);
            }

            return optionsPost.ApplyTo(query, querySettings) as IQueryable<Event>;
        }

        [ClaimsAuthorize]
        public IHttpActionResult Put([FromODataUri] System.Guid key, Delta<Event> patch)
        {
            var model = Context.Set<Event>().Find(key);
            if (model == null)
            {
                return NotFound();
            }
            patch.Put(model);
            try
            {
                Context.SaveChanges();
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!EntityExists(key))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }
            return Updated(model);
        }

        [ClaimsAuthorize]
        public IHttpActionResult Post(Event model)
        {
            if (!ModelState.IsValid)
            {
                return BadRequest(ModelState);
            }
            var proxy = Context.Set<Event>().Create<Event>();
            var configuration = new MapperConfiguration(cfg =>
                cfg.CreateMap<Event, Event>().ReverseMap());
            var mapper = configuration.CreateMapper();
            var result = mapper.Map(model, proxy);
            Context.Set<Event>().Add(result);

            try
            {
                Context.SaveChanges();
            }
            catch (Exception e)
            {
                return GetErorrRequest(e);
            }

            return Created(model);
        }

        [ClaimsAuthorize]
        [AcceptVerbs("PATCH", "MERGE")]
        public IHttpActionResult Patch([FromODataUri] System.Guid key, Delta<Event> patch)
        {
            try
            {
                var model = Context.Set<Event>().Find(key);
                if (model == null)
                {
                    return NotFound();
                }

                patch.Patch(model);
                Context.SaveChanges();

                return Updated(model);
            }
            catch (DbUpdateConcurrencyException)
            {
                if (!EntityExists(key))
                {
                    return NotFound();
                }
                else
                {
                    throw;
                }
            }
            catch (Exception e)
            {
                return GetErorrRequest(e);
            }
        }

        [ClaimsAuthorize]
        public IHttpActionResult Delete([FromODataUri] System.Guid key)
        {
            try
            {
                var model = Context.Set<Event>().Find(key);
                if (model == null)
                {
                    return NotFound();
                }

                model.DeletedDate = System.DateTime.Now;
                model.Disabled = true;
                Context.SaveChanges();

                return StatusCode(HttpStatusCode.NoContent);
            }
            catch (Exception e)
            {
                return InternalServerError(e.InnerException);
            }
        }

        private bool EntityExists(System.Guid key)
        {
            return Context.Set<Event>().Count(e => e.Id == key) > 0;
        }

        public static IEnumerable<Column> GetExportSettings()
        {
            IEnumerable<Column> columns = new List<Column>() {
                new Column() { Order = 0, Field = "Name", Header = "Event", Quoting = false },
                new Column() { Order = 1, Field = "Description", Header = "Description" },
                new Column() { Order = 2, Field = "EventType.Name", Header = "Type", Quoting = false },
                new Column() { Order = 3, Field = "MarketSegment", Header = "MarketSegment" },
            };
            return columns;
        }

        [ClaimsAuthorize]
